/**
 * Функция для определения уровня по проценту совпадений
 * Принимает число (процент) и возвращает соответствующий уровень
 */
export function getResultLevel(percent: number): ResultLevel {
  return resultLevels.find(
    level => percent >= level.minPercent && percent <= level.maxPercent
  ) || resultLevels[0]; // Если что-то пошло не так — возвращаем первый уровень
}
